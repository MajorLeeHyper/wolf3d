#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	c2;
	char	*s2;

	c2 = (char)c;
	s2 = (char*)s;
	while (*s2)
	{
		if (*s2 == c2)
			return (s2);
		s2++;
	}
	if (c2 == '\0')
		return (s2);
	return (NULL);
}
